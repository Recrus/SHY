    protected $fillable = [
        'mark',
        'review_text',
        'reviewed_at',
        'is_accepted',
    ];

    public static function getAllowedSorts(): array
    {
        return [
            'id',
            'created_at',
        ];
    }

    public static function getAllowedFilters(): array
    {
        return [];
    }
}
